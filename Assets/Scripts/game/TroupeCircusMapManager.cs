        public override MapSpaceType[][] GetPatch(MapRectangle region) {
            MapSpaceType[][] patch = new MapSpaceType[region.w][];
            for (int i = 0; i < region.w; i++) {
                patch[i] = new MapSpaceType[region.h];
                for (int j = 0; j < region.h; j++) {
